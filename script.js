function buildLoveCards() {
  const grid = document.getElementById("reasonsGrid");
  if (!grid) return;

  Object.keys(reasons).forEach((id) => {
    const r = reasons[id];
    const card = document.createElement("div");
    card.className = "love-card" + (openedCards.includes(id) ? " opened" : "");
    card.dataset.id = id;
    card.innerHTML = `
      <span class="card-number">#${id}</span>
      <div class="card-seal">${openedCards.includes(id) ? "✓" : "❤️"}</div>
      <div class="card-content">
        <h3>${r.short}</h3>
        <span>${openedCards.includes(id) ? "Opened ❤️" : "Tap to Open"}</span>
      </div>`;
    card.addEventListener("click", () => openReason(id));
    grid.appendChild(card);
  });
  updateCardsProgress();
}

function updateCardsProgress() {
  const total = Object.keys(reasons).length;
  const opened = openedCards.length;
  const pct = (opened / total) * 100;
  const bar = document.getElementById("cardsProgressBar");
  const text = document.getElementById("cardsProgressText");
  if (bar) bar.style.width = pct + "%";
  if (text) text.textContent = `${opened} of ${total} opened`;
}

function openReason(id) {
  const r = reasons[id];
  if (!r) return;

  document.getElementById("reasonNumber").textContent = `Letter ${id} of 29`;
  document.getElementById("reasonTitle").textContent = r.title;
  document.getElementById("reasonText").textContent = r.text;
  document.getElementById("reasonModal").style.display = "block";
  document.body.style.overflow = "hidden";

  if (!openedCards.includes(String(id))) {
    openedCards.push(String(id));
    localStorage.setItem("janaaOpenedCards", JSON.stringify(openedCards));
    const card = document.querySelector(`.love-card[data-id="${id}"]`);
    if (card) {
      card.classList.add("opened");
      card.querySelector(".card-seal").textContent = "✓";
      card.querySelector(".card-content span").textContent = "Opened ❤️";
    }
    updateCardsProgress();
    if (openedCards.length === 29) {
      setTimeout(() => launchConfetti(120), 500);
    }
  }

  tryPlayMusic();
}

