function startHeroTyping() {
  const el = document.getElementById("heroTyping");
  if (!el) return;
  let line = 0;
  let char = 0;
  el.textContent = "";

  function typeLine() {
    if (line >= heroLines.length) return;
    if (char < heroLines[line].length) {
      el.textContent += heroLines[line].charAt(char);
      char++;
      setTimeout(typeLine, 35);
    } else {
      line++;
      char = 0;
      if (line < heroLines.length) {
        el.textContent += "\n";
        setTimeout(typeLine, 400);
      }
    }
  }
  typeLine();
}

// ── Stats ──
function daysBetween(from, to) {
  const ms = to - from;
  return Math.floor(ms / (1000 * 60 * 60 * 24));
}

function updateStatCounts() {
  const today = new Date();
  const metOnline = new Date("2025-10-30");
  const wedding = new Date("2026-04-07");

  const pills = document.querySelectorAll(".stat-num");
  if (pills[0]) pills[0].dataset.count = daysBetween(metOnline, today);
  if (pills[1])
    pills[1].dataset.count = Math.max(0, daysBetween(wedding, today));
  if (pills[2]) pills[2].dataset.count = 29;
}

function animateStats() {
  updateStatCounts();
  document.querySelectorAll(".stat-num").forEach((el) => {
    const target = parseInt(el.dataset.count, 10);
    let current = 0;
    const step = Math.max(1, Math.floor(target / 60));
    const timer = setInterval(() => {
      current += step;
      if (current >= target) {
        current = target;
        clearInterval(timer);
      }
      el.textContent = current;
    }, 30);
  });
}

