let currentMusicIndex = -1;
let playedMusicIndexes = [];

function getNextMusicIndex() {
  if (musicPlaylist.length === 0) return -1;
  if (playedMusicIndexes.length >= musicPlaylist.length) {
    playedMusicIndexes = [];
  }
  const availableIndexes = musicPlaylist
    .map((_, index) => index)
    .filter((index) => !playedMusicIndexes.includes(index));
  const nextIndex =
    availableIndexes[Math.floor(Math.random() * availableIndexes.length)];
  playedMusicIndexes.push(nextIndex);
  return nextIndex;
}

function setMusicSource(index) {
  const audio = document.getElementById("bgMusic");
  const btn = document.getElementById("musicToggle");
  if (!audio || index < 0 || index >= musicPlaylist.length) return;
  currentMusicIndex = index;
  audio.src = musicPlaylist[index].src;
  audio.load();
  if (btn) {
    btn.title = `Play ${musicPlaylist[index].title}`;
  }
}

